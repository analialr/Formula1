package com.ironhack.raceserver.controller.impl;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.ironhack.raceserver.classes.RaceDTO;
import com.ironhack.raceserver.classes.ResponseDTO;
import com.ironhack.raceserver.client.RaceServiceClient;
import org.json.JSONArray;
import org.json.JSONException;
import org.json.JSONObject;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.ResponseStatus;
import org.springframework.web.bind.annotation.RestController;

import java.util.*;


@RestController
public class RaceControllerImpl {
    @Autowired
    private RaceServiceClient raceServiceClient;

    @GetMapping("/races/last")
    @ResponseStatus(HttpStatus.OK)
    public RaceDTO getLastRace() throws JsonProcessingException {
        ResponseDTO responseDTO = raceServiceClient.getLastRace();
        return responseDTO.getMRData().getRaceTable().getRaces().iterator().next();
    }



    public static Map<String, Object> jsonToMap(JSONObject json) throws JSONException {
        Map<String, Object> retMap = new HashMap<String, Object>();

        if(json != JSONObject.NULL) {
            retMap = toMap(json);
        }
        return retMap;
    }

    public static Map<String, Object> toMap(JSONObject object) throws JSONException {
        Map<String, Object> map = new HashMap<String, Object>();

        Iterator<String> keysItr = object.keys();
        while(keysItr.hasNext()) {
            String key = keysItr.next();
            Object value = object.get(key);

            if(value instanceof JSONArray) {
                value = toList((JSONArray) value);
            }

            else if(value instanceof JSONObject) {
                value = toMap((JSONObject) value);
            }
            map.put(key, value);
        }
        return map;
    }

    public static List<Object> toList(JSONArray array) throws JSONException {
        List<Object> list = new ArrayList<Object>();
        for(int i = 0; i < array.length(); i++) {
            Object value = array.get(i);
            if(value instanceof JSONArray) {
                value = toList((JSONArray) value);
            }

            else if(value instanceof JSONObject) {
                value = toMap((JSONObject) value);
            }
            list.add(value);
        }
        return list;
    }

}
